const mongoose = require("mongoose");

const createUserSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const createUser = mongoose.model("createUser", createUserSchema);

module.exports = createUser;
